package ar.uba.fi.hemobilling.dto.obrassociales;

import ar.uba.fi.hemobilling.dto.listasprecio.ListaPrecioDTO;




public class AsociacionObraSocialListaPrecioDTO 
{
	
	private Long id;
	private ListaPrecioDTO listaPrecio=new ListaPrecioDTO() ;
	private String desde;
	private String hasta;
	private String nombreObraSocial;
	
	
	public AsociacionObraSocialListaPrecioDTO(){
		
	}
	
	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	

	public String getDesde() {
		return desde;
	}

	public void setDesde(String desde) {
		this.desde = desde;
	}

	public String getHasta() {
		return hasta;
	}

	public void setHasta(String hasta) {
		this.hasta = hasta;
	}

	public ListaPrecioDTO getListaPrecio() {
		return listaPrecio;
	}

	public void setListaPrecio(ListaPrecioDTO listaPrecio) {
		this.listaPrecio = listaPrecio;
	}

	public String getNombreObraSocial() {
		return nombreObraSocial;
	}

	public void setNombreObraSocial(String nombreObraSocial) {
		this.nombreObraSocial = nombreObraSocial;
	}








	

}
