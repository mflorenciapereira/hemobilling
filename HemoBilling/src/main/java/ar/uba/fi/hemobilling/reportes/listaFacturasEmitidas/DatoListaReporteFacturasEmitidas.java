package ar.uba.fi.hemobilling.reportes.listaFacturasEmitidas;

import java.math.BigDecimal;

/**
 * Esta clase contiene los campos de una fila del reporte Lista de Facturas Emitidas
 * Notar que las columnas del JRXML de este reporte estan mapeadas a los atributos de esta clase
 * (ver createDynamicReport del ListaFacturasEmitidas )
 * 
 * @author Ale
 *
 */

public class DatoListaReporteFacturasEmitidas 
{
	private String fecha;
	private String nroFactura;
	private String destinatario;
	private String obraSocial;
	private BigDecimal montoObraSocial;

	private BigDecimal montoOtros;
	private String codigoContable;
	
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getNroFactura() {
		return nroFactura;
	}
	public void setNroFactura(String nroFactura) {
		this.nroFactura = nroFactura;
	}
	public String getDestinatario() {
		return destinatario;
	}
	public void setDestinatario(String destinatario) {
		this.destinatario = destinatario;
	}
	public String getObraSocial() {
		return obraSocial;
	}
	public void setObraSocial(String obraSocial) {
		this.obraSocial = obraSocial;
	}

	public String getCodigoContable() {
		return codigoContable;
	}
	public void setCodigoContable(String codigoContable) {
		this.codigoContable = codigoContable;
	}
	public BigDecimal getMontoObraSocial() {
		return montoObraSocial;
	}
	public void setMontoObraSocial(BigDecimal montoObraSocial) {
		this.montoObraSocial = montoObraSocial;
	}
	public BigDecimal getMontoOtros() {
		return montoOtros;
	}
	public void setMontoOtros(BigDecimal montoOtros) {
		this.montoOtros = montoOtros;
	}

	
	

}
