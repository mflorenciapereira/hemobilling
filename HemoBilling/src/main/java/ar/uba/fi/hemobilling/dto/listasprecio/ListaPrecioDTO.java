package ar.uba.fi.hemobilling.dto.listasprecio;


import org.springframework.util.AutoPopulatingList;


public class ListaPrecioDTO 
{
	private Long codigo=-1L;
	private String nombre;
	private	AutoPopulatingList itemsAuto=new AutoPopulatingList(ItemListaPrecioDTO.class);
	
	
	public ListaPrecioDTO(){
		
	}

	public ListaPrecioDTO(Long codigo2) {
		this.codigo=codigo2;
	}

	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	
	public Long getCodigo() {
		return codigo;
	}
	public void setCodigo(Long codigo) {
		this.codigo = codigo;
	}
	public AutoPopulatingList getItemsAuto() {
		return itemsAuto;
	}
	public void setItemsAuto(AutoPopulatingList itemsAuto) {
		this.itemsAuto = itemsAuto;
	}


	







	

}
