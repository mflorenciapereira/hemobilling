package ar.uba.fi.hemobilling.domain.factura;

import java.util.Collection;
import java.util.Date;

import ar.uba.fi.hemobilling.domain.obrassociales.ObraSocial;

public class FiltroConsultaFacturasPrestacion 
{
	private String numero;
	
	private Date fechaDesde;
	
	private Date fechaHasta;
	
	private Collection<ObraSocial> obrasSociales;

	public String getNumero() {
		return numero;
	}

	public void setNumero(String numero) {
		this.numero = numero;
	}

	public Date getFechaDesde() {
		return fechaDesde;
	}

	public void setFechaDesde(Date fechaDesde) {
		this.fechaDesde = fechaDesde;
	}

	public Date getFechaHasta() {
		return fechaHasta;
	}

	public void setFechaHasta(Date fechaHasta) {
		this.fechaHasta = fechaHasta;
	}

	public Collection<ObraSocial> getObrasSociales() {
		return obrasSociales;
	}

	public void setObrasSociales(Collection<ObraSocial> obrasSociales) {
		this.obrasSociales = obrasSociales;
	}
	

}
