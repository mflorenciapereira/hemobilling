package ar.uba.fi.hemobilling.commons.dto;

import java.io.Serializable;
import java.util.Date;

public class PacienteLaboratorioDTO implements Serializable
{
	private static final long serialVersionUID = -7770661655313795819L;
	
	private Integer id;
	private Integer numHistoriaClinica;
	private String nombre;
	private String domicilio;
	private String localidad;
	private String codPostal;
	private String provincia;
	private Date fechaNacimiento;
	private String estadoCivil;
	private String diagnostico;
	private String severidad;
	private Integer codObraSocial;
	private String numAfiliado;
	private String telefono;
	private String tipoDoc;
	private Integer numDoc;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getNumHistoriaClinica() {
		return numHistoriaClinica;
	}
	public void setNumHistoriaClinica(Integer numHistoriaClinica) {
		this.numHistoriaClinica = numHistoriaClinica;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getDomicilio() {
		return domicilio;
	}
	public void setDomicilio(String domicilio) {
		this.domicilio = domicilio;
	}
	public String getLocalidad() {
		return localidad;
	}
	public void setLocalidad(String localidad) {
		this.localidad = localidad;
	}
	public String getCodPostal() {
		return codPostal;
	}
	public void setCodPostal(String codPostal) {
		this.codPostal = codPostal;
	}
	public String getProvincia() {
		return provincia;
	}
	public void setProvincia(String provincia) {
		this.provincia = provincia;
	}
	public Date getFechaNacimiento() {
		return fechaNacimiento;
	}
	public void setFechaNacimiento(Date fechaNacimiento) {
		this.fechaNacimiento = fechaNacimiento;
	}
	public String getEstadoCivil() {
		return estadoCivil;
	}
	public void setEstadoCivil(String estadoCivil) {
		this.estadoCivil = estadoCivil;
	}
	public String getDiagnostico() {
		return diagnostico;
	}
	public void setDiagnostico(String diagnostico) {
		this.diagnostico = diagnostico;
	}
	public String getSeveridad() {
		return severidad;
	}
	public void setSeveridad(String severidad) {
		this.severidad = severidad;
	}
	public Integer getCodObraSocial() {
		return codObraSocial;
	}
	public void setCodObraSocial(Integer codObraSocial) {
		this.codObraSocial = codObraSocial;
	}
	public String getNumAfiliado() {
		return numAfiliado;
	}
	public void setNumAfiliado(String numAfiliado) {
		this.numAfiliado = numAfiliado;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public String getTipoDoc() {
		return tipoDoc;
	}
	public void setTipoDoc(String tipoDoc) {
		this.tipoDoc = tipoDoc;
	}
	public Integer getNumDoc() {
		return numDoc;
	}
	public void setNumDoc(Integer numDoc) {
		this.numDoc = numDoc;
	}
	
	
}
